#Arithmetic operations can be combined to write more complex expressions.
print(2 + 2 * 2)  # 6

#The calculation order coincides with the rules of arithmetic operations.
#To specify an order of execution, we can use parentheses.
print((2+2)*2)  # 8

#The minus operator has a unary form that negates the value or expression.
print(-10)  # -10
print(-(100 + 200))  # -300
print(-(-20))  # 20
