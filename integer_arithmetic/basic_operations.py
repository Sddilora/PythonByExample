# Operators:
# addition (+), subtraction(-), multiplication(*)
# division(/), integer division(//)
# integer division ignores the decimal part

print(10 + 10)   # 20
print(100 - 10)  # 90
print(10 * 10)   # 100
print(77 / 10)   # 7.7
print(77 // 10)  # 7

#Python raises an error if you try to divide by zero. (ZeroDivisionError)
